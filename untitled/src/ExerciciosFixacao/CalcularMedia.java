package ExerciciosFixacao;

public class CalcularMedia {
    public static void main (String[] args) {
        double numero1 = 8.5;
        double numero2 = 10.3;
        double numero3 = 6.0;

        double media = (numero1 + numero2 + numero3) / 3;

        System.out.println("A média é: " + media );
    }
}
